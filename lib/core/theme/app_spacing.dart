// lib/core/theme/app_spacing.dart

// As per the 8-point grid system specified in the design blueprint,
// this class provides standardized spacing constants.
// Developers MUST use these constants for all padding, margins, and layout spacing
// to ensure visual consistency and maintainability.
class AppSpacing {
  AppSpacing._(); // This class is not meant to be instantiated.

  /// 4.0
  static const double xsmall = 4.0;

  /// 8.0
  static const double small = 8.0;

  /// 16.0
  static const double medium = 16.0;

  /// 24.0
  static const double large = 24.0;

  /// 32.0
  static const double xlarge = 32.0;
}
